    f1 = f1_score(y_val, y_pred)
    fpr = compute_fpr(y_val, y_pred)
    feature_ratio = mask.sum() / len(mask)

    # 🔥 Dynamic weights
    alpha = 0.5 + 0.5*(1 - f1)        # more weight if F1 is low
    beta  = 0.1 + 0.2*(feature_ratio) # more penalty if many features
    gamma = 0.1 + 0.3*(fpr)           # more penalty if FPR is high

    # Normalize (important!)
    total = alpha + beta + gamma
    alpha, beta, gamma = alpha/total, beta/total, gamma/total

    fitness = alpha*(1 - f1) + beta*feature_ratio + gamma*fpr

    return fitness

