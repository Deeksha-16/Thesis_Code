def fitness_function(feature_subset):

    # Avoid empty feature subset
    if np.sum(feature_subset) == 0:
        return 1

    selected = np.where(feature_subset == 1)[0]

    Xtr = X_train[:, selected]
    Xts = X_test[:, selected]

    model = RandomForestClassifier(
        n_estimators=100,
        random_state=42,
        n_jobs=-1
    )

    model.fit(Xtr, y_train)

    pred = model.predict(Xts)

    accuracy = accuracy_score(y_test, pred)

    # Feature reduction penalty
    feature_ratio = len(selected) / X.shape[1]

    # Lower fitness is better
    fitness = (1 - accuracy) + (0.1 * feature_ratio)

    return fitness

