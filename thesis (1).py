for iteration in range(max_iterations):

    # GWO parameter decreases linearly
    a = 2 - (2 * iteration / max_iterations)

    for i in range(num_particles):

        # =================================================
        # PSO UPDATE
        # =================================================

        r1 = np.random.rand(num_features)
        r2 = np.random.rand(num_features)

        velocities[i] = (
            w * velocities[i]
            + c1 * r1 * (pbest[i] - particles[i])
            + c2 * r2 * (gbest - particles[i])
        )

        sigmoid = 1 / (1 + np.exp(-velocities[i]))

        pso_position = np.where(
            np.random.rand(num_features) < sigmoid,
            1,
            0
        )

        # =================================================
        # GWO REFINEMENT
        # =================================================

        A = 2 * a * np.random.rand(num_features) - a
        C = 2 * np.random.rand(num_features)

        D = abs(C * gbest - pso_position)

        gwo_position = gbest - A * D

        gwo_position = np.where(
            gwo_position > 0.5,
            1,
            0
        )

        particles[i] = gwo_position

        # =================================================
        # FITNESS EVALUATION
        # =================================================

        fitness = fitness_function(particles[i])

        # Update personal best
        if fitness < pbest_scores[i]:

            pbest_scores[i] = fitness

            pbest[i] = particles[i].copy()

    # =====================================================
    # GLOBAL BEST UPDATE
    # =====================================================

    best_particle = np.argmin(pbest_scores)

    if pbest_scores[best_particle] < gbest_score:

        gbest_score = pbest_scores[best_particle]

