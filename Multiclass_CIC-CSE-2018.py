RANDOM_STATE = 42
SUBSAMPLE = 80000          # use subset for speed
FILTER_TOP = 40            # top-k by mutual info
POP_SIZE = 12              # GA population
N_GEN = 12                 # GA generations
MUT_RATE = 0.15            # mutation probability
KNN_FIT_K = 5              # KNN parameter
KNN_FIT_CV = 2             # CV folds
TOP_FINAL = 20             # final top features
