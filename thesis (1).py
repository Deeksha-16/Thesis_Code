def compute_fpr(y_true, y_pred):
    tn, fp, fn, tp = confusion_matrix(y_true, y_pred).ravel()
    return fp / (fp + tn + 1e-10)

def fitness_function(mask):
    mask = np.array(mask).astype(bool)

    if mask.sum() == 0:
        return 1e6

