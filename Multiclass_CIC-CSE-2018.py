# ===============================================================
print("\n[1] Filter: mutual information (top {})".format(FILTER_TOP))
mi = mutual_info_classif(X_sub, y_sub, random_state=RANDOM_STATE)
mi_series = pd.Series(mi, index=feature_names)
selected_filter = mi_series.sort_values(ascending=False).head(FILTER_TOP).index.tolist()
print(f"Top-{FILTER_TOP} features:\n{selected_filter}\n")
X_filt = X_sub[:, [feature_names.index(f) for f in selected_filter]].astype(np.float32)

# ===============================================================
# [2] GENETIC ALGORITHM STAGE — Optimized GA
# ===============================================================
print("[2] GA Stage (optimized) — may take ~20 mins...")
random.seed(RANDOM_STATE)
np.random.seed(RANDOM_STATE)

def knn_fitness_mask(mask):
    """Fitness function using KNN accuracy."""
    cols = np.where(mask == 1)[0]
    if len(cols) < 2:   # avoid empty or 1-feature subsets
        return 0.0
    Xtmp = X_filt[:, cols]
    knn = KNeighborsClassifier(n_neighbors=KNN_FIT_K)
    try:
        score = cross_val_score(knn, Xtmp, y_sub, cv=KNN_FIT_CV, scoring='accuracy', n_jobs=2).mean()
        return float(score)
    except Exception:
        return 0.0

n_feat = len(selected_filter)
pop = [np.random.randint(0,2,n_feat).astype(np.int8) for _ in range(POP_SIZE)]
best_mask, best_score = None, -1

for g in range(1, N_GEN+1):
    fitness = np.array([knn_fitness_mask(ind) for ind in pop], dtype=np.float32)
    gen_best = fitness.max(); gen_mean = fitness.mean()

    # ✅ Safe normalization to avoid NaN
    if np.all(fitness <= 0) or np.isnan(fitness).any():
        probs = np.ones(POP_SIZE) / POP_SIZE
    else:
        probs = fitness / fitness.sum()

    if gen_best > best_score:
        best_score = gen_best
        best_mask = pop[int(np.argmax(fitness))].copy()

    print(f"Gen {g}/{N_GEN}: best={gen_best:.4f} mean={gen_mean:.4f}")

    # --- Selection, Crossover, Mutation ---
    new_pop = []
    for _ in range(POP_SIZE // 2):
        parents = np.random.choice(range(POP_SIZE), 2, p=probs)
        p1, p2 = pop[parents[0]], pop[parents[1]]
        cx = np.random.randint(1, n_feat - 1)
        c1 = np.concatenate([p1[:cx], p2[cx:]])
        c2 = np.concatenate([p2[:cx], p1[cx:]])
        for c in (c1, c2):
            r = np.random.random(size=c.shape)
            c[r < MUT_RATE] = 1 - c[r < MUT_RATE]
            new_pop.append(c.astype(np.int8))
    pop = new_pop
    gc.collect()

print(f"\n✅ GA done. Best score={best_score:.4f}")
sbm_selected = [selected_filter[i] for i, b in enumerate(best_mask) if b == 1]
