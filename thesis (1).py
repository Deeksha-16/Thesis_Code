import numpy as np

def binary_gwo(n_wolves=20, n_iter=20, mutation_rate=0.1):
    dim = X_train_fs.shape[1]
    wolves = np.random.randint(0, 2, (n_wolves, dim))
    convergence = []

    fitness_cache = {}

    def cached_fitness(mask):
        key = tuple(mask)
        if key in fitness_cache:
            return fitness_cache[key]
        score = fitness_function(mask)
        fitness_cache[key] = score
        return score

    best_global = None
    best_global_score = float('inf')

    for t in range(n_iter):
        scores = np.array([cached_fitness(w) for w in wolves])
        sorted_idx = np.argsort(scores)

        alpha = wolves[sorted_idx[0]].copy()
        beta = wolves[sorted_idx[1]].copy()
        delta = wolves[sorted_idx[2]].copy()
        alpha_score = scores[sorted_idx[0]]

        if alpha_score < best_global_score:
            best_global = alpha.copy()
            best_global_score = alpha_score

        a = 2 - 2 * (t / n_iter)
        new_wolves = []

        for i in range(n_wolves):
            X1 = np.zeros(dim)
            X2 = np.zeros(dim)
            X3 = np.zeros(dim)

            for d in range(dim):
                r1, r2 = np.random.rand(), np.random.rand()
                A1 = 2 * a * r1 - a
                C1 = 2 * r2
                D_alpha = abs(C1 * alpha[d] - wolves[i, d])
                X1[d] = alpha[d] - A1 * D_alpha

                r1, r2 = np.random.rand(), np.random.rand()
                A2 = 2 * a * r1 - a
                C2 = 2 * r2
                D_beta = abs(C2 * beta[d] - wolves[i, d])
                X2[d] = beta[d] - A2 * D_beta

                r1, r2 = np.random.rand(), np.random.rand()
                A3 = 2 * a * r1 - a
                C3 = 2 * r2
                D_delta = abs(C3 * delta[d] - wolves[i, d])
                X3[d] = delta[d] - A3 * D_delta

            X_new = (X1 + X2 + X3) / 3.0
            probs = 1 / (1 + np.exp(-X_new))
            wolf_bin = (np.random.rand(dim) < probs).astype(int)

            if np.random.rand() < mutation_rate:
                flip_idx = np.random.randint(dim)
                wolf_bin[flip_idx] = 1 - wolf_bin[flip_idx]

            if wolf_bin.sum() == 0:
                wolf_bin[np.random.randint(dim)] = 1

            new_wolves.append(wolf_bin)

        wolves = np.array(new_wolves)
        convergence.append(best_global_score)
        print(f"Iter {t+1}/{n_iter} | Best Fitness: {best_global_score:.5f}")

    return best_global, best_global_score, convergence

