# ------------------ FITNESS CACHE ------------------
fitness_cache = {}

def cached_fitness(mask):
    key = tuple(mask)
    if key in fitness_cache:
        return fitness_cache[key]

    score = fitness_function(mask)
    fitness_cache[key] = score
    return score

# ------------------ CHECKPOINT SAVE ------------------
def save_pso_checkpoint(iteration, particles, velocities, pbest, pbest_scores, gbest, gbest_score, convergence):
    checkpoint = {
        "iteration": int(iteration),
        "particles": particles.tolist(),
        "velocities": velocities.tolist(),
        "pbest": pbest.tolist(),
        "pbest_scores": pbest_scores.tolist(),
        "gbest": gbest.tolist(),
        "gbest_score": float(gbest_score),
        "convergence": [float(x) for x in convergence]
    }

    with open(f"{save_dir}/pso_checkpoint.json", "w") as f:
        json.dump(checkpoint, f, indent=4)

# ------------------ CHECKPOINT LOAD ------------------
def load_pso_checkpoint():
    path = f"{save_dir}/pso_checkpoint.json"
    if not os.path.exists(path):
        return None

    with open(path, "r") as f:
        checkpoint = json.load(f)

    checkpoint["particles"] = np.array(checkpoint["particles"])
    checkpoint["velocities"] = np.array(checkpoint["velocities"])
    checkpoint["pbest"] = np.array(checkpoint["pbest"])
    checkpoint["pbest_scores"] = np.array(checkpoint["pbest_scores"])
    checkpoint["gbest"] = np.array(checkpoint["gbest"])
    checkpoint["convergence"] = list(checkpoint["convergence"])

    return checkpoint

# ------------------ BINARY PSO ------------------
def binary_pso(n_particles=20, n_iter=20, resume=False):
    dim = X_train_fs.shape[1]

    # Resume option
    checkpoint = load_pso_checkpoint() if resume else None

    if checkpoint is not None:
        start_iter = checkpoint["iteration"] + 1
        particles = checkpoint["particles"]
        velocities = checkpoint["velocities"]
        pbest = checkpoint["pbest"]
        pbest_scores = checkpoint["pbest_scores"]
        gbest = checkpoint["gbest"]
        gbest_score = checkpoint["gbest_score"]
        convergence = checkpoint["convergence"]

        print(f"Resuming from iteration {start_iter}")
    else:
        start_iter = 0
        particles = np.random.randint(0, 2, (n_particles, dim))
        velocities = np.random.uniform(-1, 1, (n_particles, dim))

        pbest = particles.copy()
        pbest_scores = np.array([cached_fitness(p) for p in particles])

        gbest_idx = np.argmin(pbest_scores)
        gbest = pbest[gbest_idx].copy()
        gbest_score = pbest_scores[gbest_idx]
        convergence = []

    # PSO parameters
    w_max, w_min = 0.9, 0.4
    c1, c2 = 1.5, 1.5

    # Early stopping
    patience = 5
    no_improve = 0
    best_so_far = gbest_score

    for t in range(start_iter, n_iter):
        w = w_max - (w_max - w_min) * (t / n_iter)

        for i in range(n_particles):
            r1 = np.random.rand(dim)
            r2 = np.random.rand(dim)

            velocities[i] = (
                w * velocities[i]
                + c1 * r1 * (pbest[i] - particles[i])
                + c2 * r2 * (gbest - particles[i])
            )

            # Velocity clipping
            velocities[i] = np.clip(velocities[i], -4, 4)

            probs = sigmoid(velocities[i])
            particles[i] = (np.random.rand(dim) < probs).astype(int)

            # Avoid empty feature set
            if particles[i].sum() == 0:
                particles[i][np.random.randint(dim)] = 1

            score = cached_fitness(particles[i])

            if score < pbest_scores[i]:
                pbest[i] = particles[i].copy()
                pbest_scores[i] = score

                if score < gbest_score:
                    gbest = particles[i].copy()
                    gbest_score = score

        convergence.append(float(gbest_score))
        print(f"Iter {t+1}/{n_iter} | Best Fitness: {gbest_score:.5f}")

        # Early stopping check
        if gbest_score < best_so_far:
            best_so_far = gbest_score
            no_improve = 0
        else:
            no_improve += 1

        if no_improve >= patience:
            print("Early stopping triggered")
            break

        # Save checkpoint
        save_pso_checkpoint(
            iteration=t,
            particles=particles,
            velocities=velocities,
            pbest=pbest,
            pbest_scores=pbest_scores,
            gbest=gbest,
            gbest_score=gbest_score,
            convergence=convergence
        )

    # ------------------ SAVE FINAL RESULTS ------------------
    selected_features = X_train_fs.columns[np.array(gbest).astype(bool)].tolist()

    final_results = {
        "best_fitness": float(gbest_score),
        "selected_feature_count": int(np.sum(gbest)),
        "selected_features": selected_features,
        "convergence": convergence
    }

    with open(f"{save_dir}/pso_results.json", "w") as f:
        json.dump(final_results, f, indent=4)

    # ------------------ SAVE FINAL MODEL ------------------
    best_cols = X_train_fs.columns[np.array(gbest).astype(bool)]

    clf = RandomForestClassifier(
        n_estimators=100,
        max_depth=12,
        n_jobs=-1,
        random_state=42,
        class_weight='balanced'
    )

    clf.fit(X_train_fs[best_cols], y_train_opt)

    joblib.dump(clf, f"{save_dir}/best_rf_model.pkl")

    print("\n✅ Final Best Fitness:", gbest_score)
    print("✅ Selected Features:", len(selected_features))

    return gbest, gbest_score, convergence

