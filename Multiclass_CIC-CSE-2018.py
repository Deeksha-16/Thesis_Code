print("Preparing optimized GA data...")

# ✅ Convert X_scaled to DataFrame if needed
if isinstance(X_scaled, np.ndarray):
    X_df = pd.DataFrame(X_scaled, columns=[f"f{i}" for i in range(X_scaled.shape[1])])
